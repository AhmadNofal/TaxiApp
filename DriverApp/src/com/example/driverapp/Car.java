package com.example.driverapp;


public class Car {
	
	private String type ; 
	private String model ; 
	private int Luxury  ;
	private int fuel ;
	private int Status ; 
	private int seat ;
	private int number; 
	private int year_Manuf ;
	public String getType() {
		return type;
	}
	public void setType(String type) {
		this.type = type;
	}
	public String getModel() {
		return model;
	}
	public void setModel(String model) {
		this.model = model;
	}
	public int getLuxury() {
		return Luxury;
	}
	public void setLuxury(int luxury) {
		Luxury = luxury;
	}
	public int getFuel() {
		return fuel;
	}
	public void setFuel(int fuel) {
		this.fuel = fuel;
	}
	public int getStatus() {
		return Status;
	}
	public void setStatus(int status) {
		Status = status;
	}
	public int getSeat() {
		return seat;
	}
	public void setSeat(int seat) {
		this.seat = seat;
	}
	public int getNumber() {
		return number;
	}
	public void setNumber(int number) {
		this.number = number;
	}
	public int getYear_Manuf() {
		return year_Manuf;
	}
	public void setYear_Manuf(int year_Manuf) {
		this.year_Manuf = year_Manuf;
	}

}
